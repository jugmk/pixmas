package mk.jug.pixmas.display;

/**
 *
 */
public class SimulatorDefaultCapabilities {

  private final int width;
  private final int height;

  public SimulatorDefaultCapabilities(int width, int height) {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return width;
  }

  public int getHeight() {
    return height;
  }
}
